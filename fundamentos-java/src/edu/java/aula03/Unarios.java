package edu.java.aula03;

public class Unarios {
    public static void main(String[] args) {
        int numero = 5;
        numero = - numero; // Torna o número negativo.

        System.out.println(numero);

        numero = - numero;

       System.out.println(numero);
       
       numero++; //incrementa + 1
       numero++;
       numero++;
       numero--; //decrementa - 1

       System.out.println(numero);

       boolean variavel = true;

       System.out.println(!variavel); //negando expressão booleana.
    }
    
}
